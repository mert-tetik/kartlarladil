export function getAiPracticeCharacters() {
  return AI_PRACTICE_CHARACTERS;
}

export function getAiPracticeCharacter(id: string) {
  return AI_PRACTICE_CHARACTERS.find((character) => character.id === id) ?? null;
}

export function getCharacterName(character: AiPracticeCharacter, language: LanguageCode) {
  return character.namesByLanguage[language];
}

function languageNames(names: Record<LanguageCode, string>) {
  return languageRecord(names);
}

function languageRecord<T extends string>(values: Record<LanguageCode, T>) {
  const missingCode = LANGUAGE_CODES.find((code) => !values[code]);

  if (missingCode) {
    throw new Error(`Missing AI practice language value for ${missingCode}.`);
  }

  return values;
}
